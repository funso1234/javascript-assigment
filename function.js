function add(x,y){
	return x + y;
}

function multiply(x,y){
	return x * y;
}

function division(x,y){
	return x / y;
}

function substract(x,y){
	return x - y;
}

function modulo(x,y){
	return x % y;
}

function square(x,y){
	return x ** y;
}




module.exports = {add, multiply, division, substract, modulo, square};